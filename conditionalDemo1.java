public class conditionalDemo1 {
    public static void main(String[] args) {
        
    // && Conditional-AND
    // || Conditional-OR
        int value1 = 1; 
        int value2 = 2;
        if ((value1==1) && (value2==2)) {
            System.out.println("value1 is 1 AND value2 is 2");
        }
        if ((value1==1)|| (value2==2)) {
            System.out.println("value1 is 1 OR value2 is 2");
        }
    }
}
